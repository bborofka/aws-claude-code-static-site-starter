#!/usr/bin/env bash
# Shared configuration and helpers for deploy.sh / teardown.sh.
# This file is meant to be sourced, not executed directly.

set -euo pipefail

# --- Resolve paths -----------------------------------------------------------
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ROOT_DIR="$(cd "${SCRIPT_DIR}/.." && pwd)"
SITE_DIR="${ROOT_DIR}/site"
STATE_FILE="${ROOT_DIR}/.deploy-state"

# --- Load optional config ----------------------------------------------------
# Copy config.example.sh to config.sh and edit it, or just export these vars
# in your shell before running the scripts.
if [[ -f "${ROOT_DIR}/config.sh" ]]; then
  # shellcheck disable=SC1091
  source "${ROOT_DIR}/config.sh"
fi

# --- Settings (env vars win; these are the defaults) -------------------------
PROJECT_NAME="${PROJECT_NAME:-my-static-site}"
AWS_REGION="${AWS_REGION:-us-east-1}"
# AWS_PROFILE is optional. When set, every aws call uses it. Leave it unset to
# fall back to the default credential chain.

# Make the region the default for every aws call in these scripts.
export AWS_DEFAULT_REGION="${AWS_REGION}"
# Only export AWS_PROFILE if the caller actually set one.
if [[ -n "${AWS_PROFILE:-}" ]]; then
  export AWS_PROFILE
fi

# --- Logging -----------------------------------------------------------------
log()  { printf '\033[1;34m==>\033[0m %s\n' "$*"; }
ok()   { printf '\033[1;32m✓\033[0m %s\n' "$*"; }
warn() { printf '\033[1;33m!\033[0m %s\n' "$*" >&2; }
die()  { printf '\033[1;31m✗\033[0m %s\n' "$*" >&2; exit 1; }

# --- Preflight ---------------------------------------------------------------
require_tools() {
  command -v aws >/dev/null 2>&1 || die "AWS CLI not found. Install AWS CLI v2: https://aws.amazon.com/cli/"
  command -v jq  >/dev/null 2>&1 || die "jq not found. Install it (e.g. 'brew install jq')."
}

# Validate that PROJECT_NAME is a safe S3 bucket-name component.
require_valid_project_name() {
  if [[ ! "${PROJECT_NAME}" =~ ^[a-z0-9][a-z0-9-]{1,40}[a-z0-9]$ ]]; then
    die "PROJECT_NAME '${PROJECT_NAME}' is invalid. Use 3-42 chars: lowercase letters, digits, and hyphens."
  fi
}

# Account-scoped, deterministic bucket name so re-runs are idempotent.
# Globally unique because the AWS account ID is globally unique.
bucket_name() {
  local account_id="$1"
  echo "${PROJECT_NAME}-${account_id}"
}

# --- State file (sourceable KEY=value pairs) ---------------------------------
save_state() {
  cat > "${STATE_FILE}" <<EOF
# Generated by deploy.sh — do not edit by hand.
BUCKET="${BUCKET}"
OAC_ID="${OAC_ID}"
DISTRIBUTION_ID="${DISTRIBUTION_ID}"
DISTRIBUTION_DOMAIN="${DISTRIBUTION_DOMAIN}"
AWS_REGION="${AWS_REGION}"
ACCOUNT_ID="${ACCOUNT_ID}"
EOF
}

load_state() {
  [[ -f "${STATE_FILE}" ]] || return 1
  # shellcheck disable=SC1090
  source "${STATE_FILE}"
}
